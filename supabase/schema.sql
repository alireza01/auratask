-- FUNCTION: is_admin()
CREATE OR REPLACE FUNCTION public.is_admin()
RETURNS boolean AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.user_settings 
    WHERE id = auth.uid() AND username = 'admin'
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

